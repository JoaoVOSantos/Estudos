# Operadores em Python
# + = Somar
# - = Subtrair
# * = Multiplicar
# /  Dividir
# ** = Potenciação/exponenciação

numero1 = 10
numero2 = 20


resultado = numero1 + numero2
print(resultado)

resultado = numero1 - numero2
print(resultado)

resultado = numero1 * numero2
print(resultado)

resultado = numero1 / numero2
print(resultado)

resultado = numero1 ** numero2
print(resultado)


