# String

String1 = 'ola mundo'
String2 = "ola mundo"

#  int

int1 = 1
int2 = 100000

# float

float1 = 1.0
float2 = 1000.0

# boolean

boolean1 = True
boolean2 = False

print(String1 + String2)

print(int1 + int2)

print(float1 + float2)

print(boolean1 + boolean2)

# Como saber o tipo da variavel ou dado
# str , int , float , bool

print(type('ola mundo') + type(String1))
print(type(1) + type(int1))

print(type(5.0) + type(float1))

print(type(False) + type(boolean1))






